#include "libft.h"

char	*ft_itoa(int n)
{
	char	*s;
	int		i;
	int		t;

	t = n;
	i = 0;
	while (t > 9)
	{
		 t = t / 10;
		 i++;
	}
	i++;
	s = (char*)malloc(sizeof(*s) *(i + 1));
	s[i] = '\0';
	i--;
	while (i >= 0)
	{
		s[i] = (n % 10) + '0';
		n = n / 10;
		i--;
	}
	return (s);
}
