#include "libft.h"

void	ft_putnum(int x)
{
	if (x < 0)
	{
		ft_putchar('-');
		x = x * (-1);
	}
	if (x > 9)
	{
		ft_putnum(x / 10);
		ft_putnum(x % 10);
	}
	else if (x < 10)
	{
		ft_putchar(x + '0');
	}
}
