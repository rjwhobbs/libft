#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	int len;
	int i;
	char *dup;

	len = ft_strlen(s1);
	i = 0;
	dup = (char*)malloc(sizeof(*dup) * (len + 1));
	while (s1[i] != '\0')
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
